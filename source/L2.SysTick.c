#include <L2.h>

//------------------------------------------------------------------------------------------------------
uint32_t count_mseg = 0;
//------------------------------------------------------------------------------------------------------
// interrupciones
//------------------------------------------------------------------------------------------------------
void SysTick_Handler(void)
{
    if (count_mseg != 0) count_mseg--;
    Tick_1mseg_interrupt();
}
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
// funciones
//------------------------------------------------------------------------------------------------------
void Delay_mseg(uint32_t ms)
{
	count_mseg = ms;
    while(count_mseg != 0);
}
//------------------------------------------------------------------------------------------------------
void SysTick_Init(uint32_t div){
    (void) SysTick_Config(div);
}
