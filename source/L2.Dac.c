#include <L2.h>
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
// Inicializa DAC
//------------------------------------------------------------------------------------------------------
void DAC0_Init(void){
	dac_config_t dacConfigStruct;
	CLOCK_EnableClock(kCLOCK_Swm);
    SWM_SetFixedPinSelect(SWM0, kSWM_DAC_OUT0, true);	    // DACOUT0 connect to P0_17
    CLOCK_DisableClock(kCLOCK_Swm);

    POWER_DisablePD(kPDRUNCFG_PD_DAC0);					    // Power on the DAC0
    DAC_GetDefaultConfig(&dacConfigStruct);
    DAC_Init(DAC0, &dacConfigStruct);
}
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
// Inicializa DAC
//------------------------------------------------------------------------------------------------------
void DAC1_Init(void){
	dac_config_t dacConfigStruct;
	CLOCK_EnableClock(kCLOCK_Swm);
    SWM_SetFixedPinSelect(SWM0, kSWM_DAC_OUT1, true);    	// DACOUT1 connect to P0_29
    CLOCK_DisableClock(kCLOCK_Swm);

    POWER_DisablePD(kPDRUNCFG_PD_DAC1);					    // Power on the DAC1
    DAC_GetDefaultConfig(&dacConfigStruct);
    DAC_Init(DAC1, &dacConfigStruct);
}
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
// escribe DAC
//------------------------------------------------------------------------------------------------------
void DAC0_Write(uint32_t dat){
	DAC_SetBufferValue(DAC0, dat);
}
//------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------
// escribe DAC
//------------------------------------------------------------------------------------------------------
void DAC1_Write(uint32_t dat){
	DAC_SetBufferValue(DAC1, dat);
}
